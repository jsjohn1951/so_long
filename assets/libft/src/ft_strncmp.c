#include <stdlib.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	res;

	res = 0;
	while (n > 0 && *s1 != '\0' && *s2 != '\0')
	{
		if (*s1 != *s2)
			break ;
		s1++;
		s2++;
		n--;
	}
	if (n == 0)
	{
		return (0);
	}
	res = *(unsigned char *)s1 - *(unsigned char *)s2;
	return (res);
}
